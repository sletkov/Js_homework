let a = 2;
let b = "Hello world"
let c = true;
let d = null;

console.log(a+b); // number + String = String
console.log(a+c); // number + boolean = number
console.log(a+d); // number + null = number
console.log(b+c); // String + boolean = String
console.log(b+d); // String + null = String
console.log(c+d); // boolean + null = number
